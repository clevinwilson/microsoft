
function openNav() {
    let openbtn = document.getElementById("open-btn");
    let closebtn = document.getElementById("close-btn");
    openbtn.style.display = "none"
    closebtn.style.display = "block";
}

function closeNav() {
    let openbtn = document.getElementById("open-btn");
    let closebtn = document.getElementById("close-btn");
    closebtn.style.display = "none";
    openbtn.style.display = "block"
}
